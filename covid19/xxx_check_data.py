from collections import Counter

def digit_hist(data):
	digit_cnt = Counter()
	cnt = 0
	for x in data:
		for d in str(x):
			digit_cnt[int(d)] += 1
			cnt += 1
	out = []
	for i in range(10):
		x = digit_cnt[i] / cnt
		out.append(x)
	return out

def pprint_digit_hist(data):
	for d,f in enumerate(digit_hist(data)):
		s = int(f*100) * "*"
		print(f"{d} -> {f:.03f} {s}")

